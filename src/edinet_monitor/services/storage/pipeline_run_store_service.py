from __future__ import annotations

import json
import sqlite3
from typing import Any


def _to_json_text(value: Any) -> str:
    return json.dumps(value, ensure_ascii=False, sort_keys=True)


def upsert_pipeline_run(
    conn: sqlite3.Connection,
    *,
    run_id: str,
    run_type: str,
    started_at: str,
    finished_at: str,
    elapsed_seconds: float,
    run_status: str,
    run_error: str = "",
    target_date: str = "",
    date_from: str = "",
    date_to: str = "",
    manifest_prefix: str = "",
    manifest_granularity: str = "",
    requested_download_profile: str = "",
    download_auto_peak_threshold: int = 0,
    prepare_only: bool = False,
    overwrite_manifests: bool = False,
    chunks: int = 0,
    manifest_rows_total: int = 0,
    downloaded_total: int = 0,
    existing_total: int = 0,
    error_total: int = 0,
    cooldown_total: int = 0,
    download_elapsed_seconds: float = 0.0,
    retry_wait_elapsed_seconds: float = 0.0,
    cooldown_elapsed_seconds: float = 0.0,
    effective_profile_totals: dict[str, Any] | None = None,
    error_type_totals: dict[str, Any] | None = None,
    raw_retention_summary: dict[str, Any] | None = None,
    summary: dict[str, Any] | None = None,
) -> None:
    conn.execute(
        """
        INSERT INTO pipeline_runs (
            run_id,
            run_type,
            started_at,
            finished_at,
            elapsed_seconds,
            run_status,
            run_error,
            target_date,
            date_from,
            date_to,
            manifest_prefix,
            manifest_granularity,
            requested_download_profile,
            download_auto_peak_threshold,
            prepare_only,
            overwrite_manifests,
            chunks,
            manifest_rows_total,
            downloaded_total,
            existing_total,
            error_total,
            cooldown_total,
            download_elapsed_seconds,
            retry_wait_elapsed_seconds,
            cooldown_elapsed_seconds,
            effective_profile_totals_json,
            error_type_totals_json,
            raw_retention_summary_json,
            summary_json,
            created_at,
            updated_at
        )
        VALUES (
            :run_id,
            :run_type,
            :started_at,
            :finished_at,
            :elapsed_seconds,
            :run_status,
            :run_error,
            :target_date,
            :date_from,
            :date_to,
            :manifest_prefix,
            :manifest_granularity,
            :requested_download_profile,
            :download_auto_peak_threshold,
            :prepare_only,
            :overwrite_manifests,
            :chunks,
            :manifest_rows_total,
            :downloaded_total,
            :existing_total,
            :error_total,
            :cooldown_total,
            :download_elapsed_seconds,
            :retry_wait_elapsed_seconds,
            :cooldown_elapsed_seconds,
            :effective_profile_totals_json,
            :error_type_totals_json,
            :raw_retention_summary_json,
            :summary_json,
            :created_at,
            :updated_at
        )
        ON CONFLICT(run_id) DO UPDATE SET
            run_type = excluded.run_type,
            started_at = excluded.started_at,
            finished_at = excluded.finished_at,
            elapsed_seconds = excluded.elapsed_seconds,
            run_status = excluded.run_status,
            run_error = excluded.run_error,
            target_date = excluded.target_date,
            date_from = excluded.date_from,
            date_to = excluded.date_to,
            manifest_prefix = excluded.manifest_prefix,
            manifest_granularity = excluded.manifest_granularity,
            requested_download_profile = excluded.requested_download_profile,
            download_auto_peak_threshold = excluded.download_auto_peak_threshold,
            prepare_only = excluded.prepare_only,
            overwrite_manifests = excluded.overwrite_manifests,
            chunks = excluded.chunks,
            manifest_rows_total = excluded.manifest_rows_total,
            downloaded_total = excluded.downloaded_total,
            existing_total = excluded.existing_total,
            error_total = excluded.error_total,
            cooldown_total = excluded.cooldown_total,
            download_elapsed_seconds = excluded.download_elapsed_seconds,
            retry_wait_elapsed_seconds = excluded.retry_wait_elapsed_seconds,
            cooldown_elapsed_seconds = excluded.cooldown_elapsed_seconds,
            effective_profile_totals_json = excluded.effective_profile_totals_json,
            error_type_totals_json = excluded.error_type_totals_json,
            raw_retention_summary_json = excluded.raw_retention_summary_json,
            summary_json = excluded.summary_json,
            updated_at = excluded.updated_at
        """,
        {
            "run_id": str(run_id),
            "run_type": str(run_type),
            "started_at": str(started_at),
            "finished_at": str(finished_at),
            "elapsed_seconds": float(elapsed_seconds or 0.0),
            "run_status": str(run_status),
            "run_error": str(run_error or ""),
            "target_date": str(target_date or ""),
            "date_from": str(date_from or ""),
            "date_to": str(date_to or ""),
            "manifest_prefix": str(manifest_prefix or ""),
            "manifest_granularity": str(manifest_granularity or ""),
            "requested_download_profile": str(requested_download_profile or ""),
            "download_auto_peak_threshold": int(download_auto_peak_threshold or 0),
            "prepare_only": int(bool(prepare_only)),
            "overwrite_manifests": int(bool(overwrite_manifests)),
            "chunks": int(chunks or 0),
            "manifest_rows_total": int(manifest_rows_total or 0),
            "downloaded_total": int(downloaded_total or 0),
            "existing_total": int(existing_total or 0),
            "error_total": int(error_total or 0),
            "cooldown_total": int(cooldown_total or 0),
            "download_elapsed_seconds": float(download_elapsed_seconds or 0.0),
            "retry_wait_elapsed_seconds": float(retry_wait_elapsed_seconds or 0.0),
            "cooldown_elapsed_seconds": float(cooldown_elapsed_seconds or 0.0),
            "effective_profile_totals_json": _to_json_text(effective_profile_totals or {}),
            "error_type_totals_json": _to_json_text(error_type_totals or {}),
            "raw_retention_summary_json": _to_json_text(raw_retention_summary or {}),
            "summary_json": _to_json_text(summary or {}),
            "created_at": str(started_at),
            "updated_at": str(finished_at or started_at),
        },
    )


